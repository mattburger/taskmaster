package com.mjbmjb.cf.taskmaster.taskmaster.model;

import com.amazonaws.services.dynamodbv2.datamodeling.*;

@DynamoDBTable(tableName = "taskmaster")
public class TaskMaster {

    private String id;
    private String title;
    private String description;
    private String status;
    private int statusTracker;

    public TaskMaster(){}

    public TaskMaster(String title, String description){
        this.title = title;
        this.description = description;
        this.statusTracker = 0;
    }

    //getters
    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return this.id;
    }

    @DynamoDBAttribute
    public String getTitle() {
        return this.title;
    }

    @DynamoDBAttribute
    public String getDescription() {
        return this.description;
    }

    @DynamoDBAttribute
    public String getStatus() {
        return this.status;
    }

    @DynamoDBAttribute
    public int getStatusTracker() {
        return this.statusTracker;
    }

    //setters
    public void setId(String id) {
        this.id = id;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public void setStatusTracker(int statusTracker) {
        this.statusTracker = statusTracker;
    }
}
